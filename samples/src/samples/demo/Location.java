package samples.demo;

public class Location {
	
	private int Loc;
	private String LocName;
	
	
	public String getLocName() {
		return LocName;
	}

	public void setLocName(String locName) {
		LocName = locName;
	}

	public int getLoc() {
		return Loc;
	}

	public void setLoc(int loc) {
		Loc = loc;
	}

}
